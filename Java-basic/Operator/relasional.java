import java.util.*;

 // Compiler version JDK 11.0.2

 class  relasional
 {
   public static void main(String args[])
   { 
     int A=50;
     int B=60;
     //==:sama dengan
     System.out.println("A:"+(A));
     System.out.println("B:"+(B));
     System.out.println("A==B:"+(A == B));
     //!==:Tidak sama dengan
     
     System.out.println("A!=B:"+(A != B));
     
     System.out.println("A<B:"+(A <B));
      //<:Kurang
      
       //>:Lebih
     System.out.println("A>B:"+(A >B));
//<=:kurang dari

//>=:lebih dari
     System.out.println("A<=B:"+(A<= B));
     System.out.println("A>=B:"+(A>=B));
     
     
     
     
   }
 }
